create policy "users read their stats"
on public.user_stats for select to authenticated
using (user_id = auth.uid());

drop policy if exists "users read their xp" on public.xp_ledger;
create policy "users read their xp"
on public.xp_ledger for select to authenticated
using (user_id = auth.uid());

