create extension if not exists pg_trgm;

create table if not exists public.attempts (
  id bigint generated by default as identity primary key,
  challenge_id bigint not null,
  user_id uuid not null references auth.users(id) on delete cascade,
  answer_raw text not null default '',
  answer_normalized text not null default '',
  is_correct boolean not null default false,
  is_pass boolean not null default false,
  is_valid boolean not null default false,
  is_daily_case boolean not null default false,
  similarity_score numeric(5, 4) not null default 0,
  xp_earned integer not null default 0,
  combo_after integer not null default 0,
  created_at timestamptz not null default now(),
  unique (challenge_id, user_id)
);

create table if not exists public.user_stats (
  user_id uuid primary key references auth.users(id) on delete cascade,
  interpreter_xp integer not null default 0,
  uploader_xp integer not null default 0,
  activity_streak integer not null default 0,
  last_contribution_date date,
  daily_valid_activity_count integer not null default 0,
  daily_activity_date date,
  daily_bonus_date date,
  current_combo integer not null default 0,
  max_combo integer not null default 0,
  combo_date date,
  updated_at timestamptz not null default now()
);

create table if not exists public.xp_ledger (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  track text not null check (track in ('interpreter', 'uploader')),
  event_type text not null,
  event_key text not null,
  amount integer not null,
  challenge_id bigint,
  created_at timestamptz not null default now(),
  unique (user_id, event_key)
);

create table if not exists public.daily_cases (
  id bigint generated by default as identity primary key,
  challenge_id bigint not null,
  case_date date not null unique,
  selection_type text not null default 'manual' check (selection_type in ('manual', 'fallback')),
  status text not null default 'scheduled' check (status in ('scheduled', 'active', 'ended')),
  created_at timestamptz not null default now()
);

create table if not exists public.notifications (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  type text not null,
  challenge_id bigint,
  title text not null,
  body text not null,
  read_at timestamptz,
  created_at timestamptz not null default now(),
  unique (user_id, type, challenge_id)
);

alter table public.attempts enable row level security;
alter table public.user_stats enable row level security;
alter table public.xp_ledger enable row level security;
alter table public.daily_cases enable row level security;
alter table public.notifications enable row level security;

drop policy if exists "attempts visible to participant or author" on public.attempts;
create policy "attempts visible to participant or author"
on public.attempts for select to authenticated
using (
  user_id = auth.uid()
  or exists (
    select 1 from public.challenges
    where challenges.id = attempts.challenge_id
      and challenges.author_id = auth.uid()
  )
);

